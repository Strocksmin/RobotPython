def main(x, y, z):
    total = 0
    n = len(x)
    x.insert(0, 0)
    y.insert(0, 0)
    z.insert(0, 0)
    for i in range(1, n + 1):
        total += (math.log(90 * y[n + 1 - math.ceil(i / 3)] + x[n + 1 - math.ceil(i / 2)]
                           ** 3 + 38 * z[n + 1 - i] ** 2, 2) ** 2) / 41
    return total


